/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* init code for USB_DEVICE */
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN StartDefaultTask */
  uint16_t data[100000][4];
  for(int i=0; i<100000; i++){
	  uint64_t tmp = i + i<<16 + i<<32 + i<<48;
	  memcpy(data+i, &tmp, 8);
  }
  /* Infinite loop */
  for(;;)
  {
	  if(ReceivedDataFlag == 2){
		  ReceivedDataFlag = 0;
		  for(int i=0; i<100000; i++){
			  uint8_t TmpBuffer[11];
			  mempcy(TmpBuffer, i, 3);
			  memcpy(TmpBuffer+3, data, 8);
			  CDC_Transmit_FS(TmpBuffer, 11);
		  }
	  }
	  else if(ReceivedDataFlag == 1){
		  MessageLength = sprintf(DataToSend, "Odebrano: %s\n\r", ReceivedData);
		  CDC_Transmit_FS(DataToSend, MessageLength);
	  }
//	  HAL_Delay(1000);
    osDelay(1000);
  }
  /* USER CODE END StartDefaultTask */
}

